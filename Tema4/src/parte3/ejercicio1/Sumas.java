package parte3.ejercicio1;

/**
 * La clase Sumas proporciona métodos para realizar sumas de números enteros y decimales.
 */
public class Sumas {

    /**
     * Suma dos números enteros.
     *
     * @param n1 el primer número entero
     * @param n2 el segundo número entero
     * @return la suma de los dos números enteros
     */
    static public int suma(int n1, int n2) {
        return (n1 + n2);
    }

    /**
     * Suma dos números de tipo double.
     *
     * @param n1 el primer número decimal
     * @param n2 el segundo número decimal
     * @return la suma de los dos números decimales
     */
    static public double suma(double n1, double n2) {
        return (n1 + n2);
    }

}
